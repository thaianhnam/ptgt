    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validdateEmpty(txtMaLoaiXe, sb, "Mã loại xe không được bỏ trống");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }

        if (MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn xóa loại xe không?", "Hỏi") == JOptionPane.NO_OPTION) {
            return;
        }

        try {

            boolean checkMLX = false;
            ThongTinXeDao ttxd = new ThongTinXeDao();
            List<ThongTinXe> lttx = ttxd.findAll();
            for(ThongTinXe ttx : lttx){
                if(ttx.getMaXM() != null){
                    if(ttx.getMaXM().trim().equalsIgnoreCase(txtMaLoaiXe.getText().trim())){
                    checkMLX = true;
                    break;
                    }
                }
            }
            
            XeMayDao dao = new XeMayDao();
            if(checkMLX == true){
                MessageDialogHelper.showMessageDialog(parentForm, "Vui lòng xóa mã loại xe này trong bảng thông tin xe!", "Cảnh báo");
                return;
            }else{
                if (dao.delete(txtMaLoaiXe.getText())) {
                    MessageDialogHelper.showMessageDialog(parentForm, "Xe đã được xóa", "Thông báo");
                    btnXoaTrangActionPerformed(evt);
                    loadDataToTable();
                } else {
                    MessageDialogHelper.showConfirmDialog(parentForm, "Xe không được xóa do lỗi", "Cảnh báo");
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

