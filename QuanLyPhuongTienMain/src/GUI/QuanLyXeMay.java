    private void tblXeMayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblXeMayMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblXeMay.getSelectedRow();

            if (row >= 0) {
                String id = (String) tblXeMay.getValueAt(row, 0);
                XeMayDao dao = new XeMayDao();
                XeMay lx = dao.findById(id);

                if (lx != null) {
                    txtMaLoaiXe.setText(lx.getMaXM());
                    txtTenLoaiXe.setText(lx.getTenXe());
                    txtHangSanXuat.setText(lx.getHangSanXuat());
                    txtNamSanXuat.setText(lx.getNamSanXuat());
                    txtGiaBan.setText(lx.getGiaBan());
                    txtMau.setText(lx.getMauSon());
                    txtCongSuat.setText(""+lx.getCongSuat());
               
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblXeMayMouseClicked

