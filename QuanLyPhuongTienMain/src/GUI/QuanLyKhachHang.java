    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validdateEmpty(txtMaKH, sb, "Mã khách hàng không được để trống !");
        DataValidator.validdateEmpty(txtHoTen, sb, "Tên khách hàng không được để trống !");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return;
        }
        if (MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn cập nhật nhân viên không", "Hỏi?") == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            KhachHang kh = new KhachHang();
            kh.setMaKH(txtMaKH.getText());
            kh.setHoTen(txtHoTen.getText());
            kh.setGioiTinh(rdoNam.isSelected() ? 1 : 0);
            kh.setCMND(Integer.parseInt(txtCMND.getText()));
            kh.setNgaySinh(txtNgaySinh.getText());
            kh.setEmail(txtEmail.getText());
            kh.setDienThoai(txtDienThoai.getText());
            kh.setDiaChi(jtxtDiaChi.getText());

            KhachHangDao dao = new KhachHangDao();
            if (dao.update(kh)) {
                MessageDialogHelper.showMessageDialog(parentForm, "Khách hàng đã đươc cập nhật", "Thông báo !");
                btnResetActionPerformed(evt);
                LoadDataTable();
            } else {
                MessageDialogHelper.showConfirmDialog(parentForm, "Khách hàng không được cập nhật do lỗi !", "Cảnh báo!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validdateEmpty(txtMaKH, sb, "Mã khách hàng không được để trống !");

        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return;
        }
        if (MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn xóa khách hàng không", "Hỏi?") == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            PhieuThueDao ptdao = new PhieuThueDao();
            List<PhieuThue> listpt = ptdao.findAll();
            boolean checkPT = false;
            for (PhieuThue phieuThue : listpt) {
                if (phieuThue.getMaKH().trim().equalsIgnoreCase(txtMaKH.getText().trim())) {
                    checkPT = true;
                    break;
                }
            }
            if (checkPT == false) {
                KhachHangDao dao = new KhachHangDao();
                if (dao.delete(txtMaKH.getText())) {
                    MessageDialogHelper.showMessageDialog(parentForm, "Khách hàng đã đươc xóa!", "Thông báo !");
                    btnResetActionPerformed(evt);
                    LoadDataTable();
                } else {
                    MessageDialogHelper.showConfirmDialog(parentForm, "Khách hàng không được xóa do lỗi !", "Cảnh báo!");
                }
            }else{
                MessageDialogHelper.showMessageDialog(parentForm, "Khách hàng đang thuê xe không được xóa !", "Cảnh báo!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

