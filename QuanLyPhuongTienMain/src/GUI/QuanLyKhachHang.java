    private void LoadDataTable() {
        try {
            KhachHangDao dao = new KhachHangDao();
            List<KhachHang> list = dao.findAll();
            tblModel.setRowCount(0);
            int NO = 0;
            for (KhachHang kh : list) {
                NO++;
                tblModel.addRow(new Object[]{
                    NO, kh.getMaKH(), kh.getHoTen(), kh.getGioiTinh() == 1 ? "Nam" : "Nữ", kh.getCMND(), kh.getNgaySinh(), kh.getEmail(), kh.getDienThoai(), kh.getDiaChi()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }

    private void LoadDataTableByID() {
        try {
            KhachHangDao dao = new KhachHangDao();
            KhachHang kh = dao.findByID(txtMaKHTimKiem.getText());
            tblModel.setRowCount(0);
            int NO = 1;
            tblModel.addRow(new Object[]{
                NO, kh.getMaKH(), kh.getHoTen(), kh.getGioiTinh() == 1 ? "Nam" : "Nữ", kh.getCMND(), kh.getNgaySinh(), kh.getEmail(), kh.getDienThoai(), kh.getDiaChi()
            });

            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }

