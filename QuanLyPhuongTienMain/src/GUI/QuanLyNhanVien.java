    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validdateEmpty(txtMaNV, sb, "Mã nhân viên không được để trống !");
        DataValidator.validdateEmpty(txtTenNV, sb, "Tên nhân viên không được để trống !");
        DataValidator.validdateEmpty(txtUserName, sb, "UserName không được để trống !");
        DataValidator.validdateEmpty(txtUserPass, sb, "UserPass không được để trống !");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return;
        }
        try {
            NguoiDung nd = new NguoiDung();
            nd.setMaND(txtMaNV.getText().trim());
            nd.setTenND(txtTenNV.getText());
            nd.setTuoi(Integer.parseInt(txtTuoi.getText()));
            nd.setSoDT(txtDienThoai.getText());
            nd.setGioiTinh(rdoNam.isSelected() ? 1 : 0);
            nd.setDiaChi(jtxtDiaChi.getText());
            nd.setChucVu(cbxChucVu.getSelectedItem().toString());
            nd.setUserName(txtUserName.getText());
            nd.setUserPass(txtUserPass.getText());
            nd.setHinh(personalImage);
            NhanVienDao dao = new NhanVienDao();
            boolean CheckMNV = false;
            List<NguoiDung> lnd = dao.findAll();
            for (NguoiDung nguoidung : lnd) {
                if (nguoidung.getMaND().trim().equalsIgnoreCase(txtMaNV.getText().trim())) {
                    CheckMNV = true;
                };
            }
            if (CheckMNV == true) {
                MessageDialogHelper.showMessageDialog(parentForm, "Nhân viên đã tồn tại!", "Cảnh báo!");
                return;
            } else {

                if (dao.insert(nd)) {
                    MessageDialogHelper.showMessageDialog(parentForm, "Nhân viên đã đươc lưu", "Thông báo !");
                    LoadDataTable();
                    btnResetActionPerformed(evt);
                } else {
                    MessageDialogHelper.showConfirmDialog(parentForm, "Nhân viên không được lưu do lỗi !", "Cảnh báo!");
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }//GEN-LAST:event_btnThemMoiActionPerformed

