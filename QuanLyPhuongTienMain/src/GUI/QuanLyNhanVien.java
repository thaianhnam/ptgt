    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validdateEmpty(txtMaNV, sb, "Mã nhân viên không được để trống !");

        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return;
        }
        if (MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn xóa nhân viên không", "Hỏi?") == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            PhieuThueDao ptdao = new PhieuThueDao();
            List<PhieuThue> listpt = ptdao.findAll();
            boolean checkPT = false;
            for (PhieuThue phieuThue : listpt) {
                if (phieuThue.getMaNV().trim().equalsIgnoreCase(txtMaNV.getText().trim())) {
                    checkPT = true;
                    break;
                }
            }
            if (checkPT == false) {
                NhanVienDao dao = new NhanVienDao();
                if (dao.delete(txtMaNV.getText())) {
                    MessageDialogHelper.showMessageDialog(parentForm, "Nhân viên đã đươc xóa!", "Thông báo !");
                    btnResetActionPerformed(evt);
                    LoadDataTable();
                } else {
                    MessageDialogHelper.showConfirmDialog(parentForm, "Nhân viên không được xóa !", "Cảnh báo!");
                }
            } else {
                MessageDialogHelper.showMessageDialog(parentForm, "Nhân viên đang quản lý thuê xe không được xóa !", "Cảnh báo!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

