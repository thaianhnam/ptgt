    private void LoadDataTable() {
        try {
            NhanVienDao dao = new NhanVienDao();
            List<NguoiDung> list = dao.findAll();
            tblModel.setRowCount(0);
            int NO = 0;
            for (NguoiDung nd : list) {
                NO++;
                tblModel.addRow(new Object[]{
                    NO, nd.getMaND(), nd.getTenND(), nd.getTuoi(),
                    nd.getSoDT(), nd.getGioiTinh() == 1 ? "Nam" : "Nữ",
                    nd.getDiaChi(), nd.getChucVu(), nd.getUserName(), nd.getUserPass()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }

    private void LoadDataTableByID() {
        try {
            NhanVienDao dao = new NhanVienDao();
            NguoiDung nd = dao.findByID(txtMaNVTimKiem.getText());
            tblModel.setRowCount(0);
            int NO = 1;
            tblModel.addRow(new Object[]{
                NO, nd.getMaND(), nd.getTenND(), nd.getTuoi(),
                nd.getSoDT(), nd.getGioiTinh() == 1 ? "Nam" : "Nữ",
                nd.getDiaChi(), nd.getChucVu(), nd.getUserName(), nd.getUserPass()
            });

            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }

