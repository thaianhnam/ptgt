    private void btnMoHinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMoHinhActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(new FileFilter() {
            public boolean accept(File f) {
                if (f.isDirectory()) {
                    return true;
                } else {
                    return f.getName().toLowerCase().endsWith(".jpg");
                }
            }

            @Override
            public String getDescription() {
                return " Image File (*.jpg)";
            }
        });
        // hop thoai
        if (chooser.showOpenDialog(parentForm) == JFileChooser.CANCEL_OPTION) {
            return;
        }
        File file = chooser.getSelectedFile();
        try {
            ImageIcon icon = new ImageIcon(file.getPath());
            Image img = ImageHelper.resize(icon.getImage(), 140, 150);
            ImageIcon resiIcon = new ImageIcon(img);
            lblImage.setIcon(resiIcon);
            //luu vao CSDL
            personalImage = ImageHelper.toByteArray(img, "jpg");
        } catch (IOException e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }//GEN-LAST:event_btnMoHinhActionPerformed

