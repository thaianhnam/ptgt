    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblNhanVien.getSelectedRow();
            if (row >= 0) {
                String id = (String) tblNhanVien.getValueAt(row, 1);
                NhanVienDao dao = new NhanVienDao();
                NguoiDung nd = dao.findDung(id);
                if (nd != null) {
                    txtMaNV.setText(nd.getMaND());
                    txtTenNV.setText(nd.getTenND());
                    txtTuoi.setText("" + nd.getTuoi());
                    txtDienThoai.setText(nd.getSoDT());
                    jtxtDiaChi.setText(nd.getDiaChi());
                    txtUserName.setText(nd.getUserName());
                    txtUserPass.setText(nd.getUserPass());
                    cbxChucVu.setSelectedItem(nd.getChucVu().trim());
                    rdoNu.setSelected(nd.getGioiTinh() == 1 ? false : true);
                    rdoNam.setSelected(nd.getGioiTinh() == 1 ? true : false);
                    if (nd.getHinh() != null) {
                        Image img = ImageHelper.createiImage(nd.getHinh(), "jpg");
                        lblImage.setIcon(new ImageIcon(img));
                        personalImage = nd.getHinh();
                    } else {
                        personalImage = nd.getHinh();
                        ImageIcon icon = new ImageIcon(
                                getClass().getResource(""));
                        lblImage.setIcon(icon);
                    }

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi!");
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked

