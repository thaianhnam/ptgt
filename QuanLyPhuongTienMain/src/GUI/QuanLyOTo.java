    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validdateEmpty(txtMaLoaiXe, sb, "Mã loại xe không được bỏ trống");
        DataValidator.validdateEmpty(txtTenLoaiXe, sb, "Tên loại xe không được bỏ trống");
        DataValidator.validdateEmpty(txtNamSanXuat, sb, "Năm sản xuất không được bỏ trống");
        DataValidator.validdateEmpty(txtGiaBan, sb, "Giá bán không được bỏ trống");
        DataValidator.validdateEmpty(txtMau, sb, "Màu xe không được bỏ trống");
        DataValidator.validdateEmpty(txtHangSanXuat, sb, "Hãng sản xuất không được bỏ trống");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try {
            OTo lx = new OTo();
            lx.setMaOT(txtMaLoaiXe.getText().trim());
            lx.setTenXe(txtTenLoaiXe.getText());
            lx.setHangSanXuat(txtHangSanXuat.getText());
            lx.setNamSanXuat(txtNamSanXuat.getText());
            lx.setGiaBan(txtGiaBan.getText());
            lx.setMauSon(txtMau.getText());
            lx.setKieuDongCo(txtKieuDongCo.getText());
            lx.setSoChoNgoi(Integer.parseInt(txtSoChoNgoi.getText()));
            OToDao dao = new OToDao();
            boolean CheckOTo = false;
            List<OTo> lot = dao.findAll();
            for (OTo oto : lot) {
                if (oto.getMaOT().trim().equalsIgnoreCase(txtMaLoaiXe.getText().trim())) {
                    CheckOTo = true;
                };
            }
            if (CheckOTo == true) {
                MessageDialogHelper.showMessageDialog(parentForm, "Xe đã tồn tại!", "Cảnh báo!");
                return;
            } else {
                if (dao.insert(lx)) {
                    MessageDialogHelper.showMessageDialog(parentForm, "Xe đã được lưu", "Thông báo");
                    btnXoaTrangActionPerformed(evt);
                    loadDataToTable();
                } else {
                    MessageDialogHelper.showConfirmDialog(parentForm, "Xe không được lưu do lỗi", "Cảnh báo");
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnThemActionPerformed

