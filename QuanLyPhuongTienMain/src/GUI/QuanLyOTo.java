    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        try {
            OToDao dao = new OToDao();
            List<OTo> list = dao.findByColor(txtMauSacTK.getText().trim());
            if (list.size() > 0) {
                tblModel.setRowCount(0);
                for (OTo oto : list) {
                    tblModel.addRow(new Object[]{
                        oto.getMaOT(), oto.getTenXe(), oto.getHangSanXuat(), oto.getNamSanXuat(), oto.getGiaBan(), oto.getMauSon(), oto.getKieuDongCo(), oto.getSoChoNgoi()
                    });
                    tblModel.fireTableDataChanged();
                }

            } else {
                MessageDialogHelper.showMessageDialog(parentForm, "Không tim thấy ô tô cần tìm!", "Thông báo !");
            }

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }

    }//GEN-LAST:event_btnTimKiemActionPerformed

