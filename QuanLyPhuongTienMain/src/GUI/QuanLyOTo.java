    private void loadDataToTable() {
        try {
            OToDao dao = new OToDao();
            List<OTo> list = dao.findAll();
            tblModel.setRowCount(0);
            for (OTo oto : list) {
                tblModel.addRow(new Object[]{
                    oto.getMaOT(), oto.getTenXe(), oto.getHangSanXuat(), oto.getNamSanXuat(), oto.getGiaBan(), oto.getMauSon(), oto.getKieuDongCo(), oto.getSoChoNgoi()
                });
            }

            tblModel.fireTableDataChanged();

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }

